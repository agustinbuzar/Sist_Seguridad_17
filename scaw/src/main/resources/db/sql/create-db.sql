DROP TABLE PERSON IF EXISTS;
DROP TABLE USER_ IF EXISTS;
DROP TABLE TASK IF EXISTS;

CREATE TABLE PERSON (
  ID 			INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  FIRSTNAME 	VARCHAR(30),
  LASTNAME 		VARCHAR(30),
  EMAIL  		VARCHAR(50)
);
CREATE TABLE USER_ (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	USERNAME VARCHAR(30),
	PASSWORD VARCHAR(30),
	EMAIL VARCHAR(50),
	CONDITION VARCHAR(20) DEFAULT 'COMMON_USER'
);

CREATE TABLE TASK(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
	MODE VARCHAR(20),
	FIRSTNAME_TO_ADD VARCHAR(30),
	LASTNAME_TO_ADD VARCHAR(30),
	EMAIL_TO_ADD VARCHAR(30),
	STATE VARCHAR(10) DEFAULT 'PENDIENTE',
	TARGET_REGISTER INTEGER
);